'''
Ejercicio N°21: Ingresar la base y la altura de un triángulo y hallar su área.
'''
base = int(input("Ingrese el valor de la base de su triangulo: ")) #El usuario ingresa el valor de la base del triángulo 
altura = int(input("Ingrese el valor de la altura de su triangulo: "))#El usuario ingresa el valor de la altura del triángulo
area = (base*altura)/2 #Cálculo del área del triángulo
print("El Área del Triángulo es:", area)#Imprime por consola el resultado
